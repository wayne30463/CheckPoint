package cp.json;

public class Check {
	private String act_id;
	private String stg_id;
	private String code;
	private String time;
	public void Check() {};
	public void setActId(String act_id)
	{
		this.act_id = act_id;
	}
	public String getActId()
	{
		return this.act_id;
	}
	public void setStgId(String stg_id)
	{
		this.stg_id = stg_id;
	}
	public String getStgId()
	{
		return this.stg_id;
	}
	public void setCode(String code)
	{
		this.code = code;
	}
	public String getsetCode()
	{
		return this.code;
	}
	public void setTime(String time)
	{
		this.time = time;
	}
	public String getTime()
	{
		return this.time;
	}
}
