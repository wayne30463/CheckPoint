package cp.json;

public class JoinSchool {
	private String act_id;
	private String sch_id;
	private String sch_name;
	private String year;
	private String among;
	private String st_code;
	private String ed_code;
	public void JoinSchool() {};
	public void setActId(String act_id)
	{
		this.act_id = act_id;
	}
	public String getActId()
	{
		return this.act_id;
	}
	public void setSchId(String sch_id)
	{
		this.sch_id = sch_id;
	}
	public String getSchId()
	{
		return this.sch_id;
	}
	public String getSchName()
	{
		return this.sch_name;
	}
	public void setSchName(String sch_name)
	{
		this.sch_name = sch_name;
	}
	public String getYear()
	{
		return this.year;
	}
	public void setYear(String year)
	{
		this.year = year;
	}
	public String getAmong()
	{
		return among;
	}
	public void setAmong(String among)
	{
		this.among = among;
	}
	public String getStCode()
	{
		return st_code;
	}
	public void setStCode(String st_code)
	{
		this.st_code = st_code;
	}
	public String getEdCode()
	{
		return ed_code;
	}
	public void setEtCode(String ed_code)
	{
		this.ed_code = ed_code;
	}
}
