package cp.json;

public class JoinStage {
	private String act_id;
	private String stg_id;
	private String stg_name;
	private String rank;
	public void JoinSchool() {};
	public void setActId(String act_id)
	{
		this.act_id = act_id;
	}
	public String getActId()
	{
		return this.act_id;
	}
	public void setStgId(String stg_id)
	{
		this.stg_id = stg_id;
	}
	public String getStgId()
	{
		return this.stg_id;
	}
	public void setStgName(String stg_name)
	{
		this.stg_name = stg_name;
	}
	public String getStgName()
	{
		return this.stg_name;
	}
	public void setRank(String rank)
	{
		this.rank = rank;
	}
	public String getRank()
	{
		return this.rank;
	}
}
