package cp.json;

public class Stage {
	private String id;
	private String name;
	private String memo;
	public void Stage() {}
	public void setId(String id)
	{
		this.id = id;
	}
	public String getId()
	{
		return this.id;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getName()
	{
		return this.name;
	}
	public void setMemo(String memo)
	{
		this.memo = memo;
	}
	public String getMemo()
	{
		return this.memo;
	}
}
